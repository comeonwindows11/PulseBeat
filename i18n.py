def normalize_lang(value: str):
    if value in SUPPORTED_LANGS:
        return value
    return DEFAULT_LANG


def get_lang():
    if not has_request_context():
        return DEFAULT_LANG
    cookie_lang = request.cookies.get("lang")
    if cookie_lang in SUPPORTED_LANGS:
        return cookie_lang
    best = request.accept_languages.best_match(list(SUPPORTED_LANGS))
    return normalize_lang(best or DEFAULT_LANG)


def _fr_courrielize(value: str):
    if not isinstance(value, str):
        return value
    out = value
    replacements = [
        (r"\bE-mails\b", "Courriels"),
        (r"\be-mails\b", "courriels"),
        (r"\bE-mail\b", "Courriel"),
        (r"\be-mail\b", "courriel"),
        (r"\bEmails\b", "Courriels"),
        (r"\bemails\b", "courriels"),
        (r"\bEmail\b", "Courriel"),
        (r"\bemail\b", "courriel"),
    ]
    for pattern, replacement in replacements:
        out = re.sub(pattern, replacement, out)

    grammar_fixes = [
        (r"\bL['’]courriel\b", "Le courriel"),
        (r"\bl['’]courriel\b", "le courriel"),
        (r"\bD['’]courriel\b", "De courriel"),
        (r"\bd['’]courriel\b", "de courriel"),
        (r"\bCet courriel\b", "Ce courriel"),
        (r"\bcet courriel\b", "ce courriel"),
        (r"\bAdresse courriel\b", "Adresse de courriel"),
        (r"\badresse courriel\b", "adresse de courriel"),
    ]
    for pattern, replacement in grammar_fixes:
        out = re.sub(pattern, replacement, out)
    return out


def t(key: str, lang: str | None = None, **kwargs):
    chosen = normalize_lang(lang or get_lang())
    value = TRANSLATIONS.get(chosen, {}).get(key)
    if value is None:
        value = TRANSLATIONS[DEFAULT_LANG].get(key, key)

    if kwargs:
        try:
            result = value.format(**kwargs)
        except Exception:
            result = value
    else:
        result = value

    if chosen == "fr":
        result = _fr_courrielize(result)
    return result


def tr(key: str, **kwargs):
    return t(key, get_lang(), **kwargs)
